<h2>Sekvenování DNA</h2>
<p>
Sekvenování DNA je metoda, která umožňuje určit pořadí nukleotidů (A, T, C, G) v molekule deoxyribonukleové kyseliny. Tato technologie je základem moderní molekulární biologie a genetiky. První úspěšné sekvenování DNA proběhlo v 70. letech pomocí tzv. Sangerovy metody, která byla po desetiletí standardem.
</p>
<p>
Dnes se většina sekvenačních analýz provádí pomocí technik tzv. sekvenování nové generace (Next Generation Sequencing – NGS). Tyto technologie umožňují během několika hodin získat kompletní genetický profil organismu. Jsou vysoce paralelní, rychlé a relativně levné ve srovnání s dřívějšími metodami.
</p>
<p>
Proces sekvenování typicky začíná izolací DNA z biologického vzorku. Poté dochází k přípravě knihovny (library preparation), během níž se DNA fragmentuje a připravuje k analýze. Následuje samotná sekvenační reakce a nakonec bioinformatická analýza surových dat.
</p>
<p>
Bioinformatická analýza zahrnuje několik kroků: kontrolu kvality čtení, jejich zarovnání k referenčnímu genomu, detekci variant (např. SNP – single nucleotide polymorphisms), anotaci a interpretaci. K tomu se používají nástroje jako FastQC, BWA, Bowtie, GATK, IGV a další.
</p>
<p>
Sekvenování se využívá v celé řadě oblastí: od výzkumu rakoviny, přes studium mikrobiomů, až po forenzní genetiku. V personalizované medicíně slouží k identifikaci genetických predispozic k nemocem a návrhu individuální terapie.
</p>
<p>
Sekvenování DNA představuje revoluci v biologii i medicíně. Díky ní máme přístup k "kódu života", který ovlivňuje všechno od vývoje embryí až po reakci těla na léčiva.
</p>
