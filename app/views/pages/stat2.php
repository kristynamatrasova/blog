<h2>Sekvenování DNA</h2>
<p>
Sekvenování DNA je proces určování pořadí nukleotidů (A, T, C, G) v molekule deoxyribonukleové kyseliny. Tento proces je klíčový pro porozumění genetické informace organismu.
</p>
<p>
První metoda sekvenování (tzv. Sangerova metoda) byla vyvinuta v 70. letech 20. století a umožnila sekvenovat první kompletní genomy. Dnes se používají moderní techniky tzv. sekvenování nové generace (NGS – Next Generation Sequencing), které umožňují rychlé a levné sekvenování celých genomů.
</p>
<p>
Data získaná sekvenováním jsou obrovská, proto je k jejich zpracování nezbytné využití bioinformatických nástrojů – například pro zarovnání sekvencí k referenčnímu genomu, detekci mutací nebo anotaci genů.
</p>
