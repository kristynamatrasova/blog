<h2>Proteinová struktura</h2>
<p>
Proteiny jsou základní stavební a funkční jednotky buněk. Každý protein je tvořen řetězcem aminokyselin, který se skládá do specifické prostorové struktury. Právě tato struktura určuje jeho funkci.
</p>
<p>
Existují čtyři úrovně struktury proteinů: primární (pořadí aminokyselin), sekundární (lokální uspořádání, např. α-helixy), terciární (celkový tvar proteinu) a kvartérní (komplex více podjednotek).
</p>
<p>
Predikce proteinové struktury je důležitým úkolem bioinformatiky. Používají se metody jako homology modeling, ab initio predikce nebo machine learning (např. AlphaFold). Tyto přístupy umožňují předpovědět strukturu proteinu na základě jeho sekvence, což je důležité pro výzkum léků a pochopení funkcí proteinů.
</p>
