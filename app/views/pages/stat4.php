<h2>Nástroje v bioinformatice</h2>
<p>
Bioinformatika využívá širokou škálu softwarových nástrojů a databází pro analýzu biologických dat. Tyto nástroje jsou často open-source a dostupné online, což umožňuje vědcům po celém světě sdílet data a postupy.
</p>
<p>
Mezi nejznámější nástroje patří:
<ul>
  <li><strong>BLAST</strong> – porovnávání sekvencí s databází známých sekvencí.</li>
  <li><strong>Clustal Omega</strong> – vícenásobné zarovnání sekvencí.</li>
  <li><strong>MEGA</strong> – analýza evolučních vztahů (fylogeneze).</li>
  <li><strong>UCSC Genome Browser</strong> – vizualizace genomových dat.</li>
  <li><strong>GenBank</strong> – databáze genetických sekvencí spravovaná NCBI.</li>
</ul>
</p>
<p>
Moderní bioinformatika také využívá programovací jazyky jako Python, R nebo Bash pro skriptování analýz a automatizaci pracovních postupů.
</p>
